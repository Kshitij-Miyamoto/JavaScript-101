//Program for additon of 2 numbers
function sum(x1 , x2){
    return x1 + x2 ; 
}
console.log(`The sum of the numbers is ${sum(12 , 8)}`);

//done using anonymus function
const add = function(x1 , x2){
return x1 + x2;
}
console.log(`The sum of the number is ${add(12 , 13)}`);

//Done using arrow function
const addition = (x1 , x2) =>{
    return x1 + x2;
}
console.log(`The sum is ${addition(12, 12)}`);

