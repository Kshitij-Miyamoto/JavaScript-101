let x = 10;
let y = 12.56;
let z = x + y;
console.log(z);

//Methods for JS numbers//

//x.toString
let str = x.toString();
console.log(str);

//x.toExponential
let exp = x.toExponential();
console.log(exp);

//x.toFixed
let fx = x.toFixed();
console.log(fx);

//x.toPrecision
let pr = x.toPrecision();
console.log(pr);

//x.ValueOf
let vl = x.valueOf();
console.log(vl);

//Max_Value
let f = Number.MAX_VALUE;
console.log(f);

//Min_Value
let s = Number.MIN_VALUE;
